def patch_configs():
    # Find all hub configs
    hub_configs = []
    for fn in glob.glob(os.path.join(CONFIGS_DIR, '*.json')):
        with open(fn, 'r', encoding='utf-8') as f:
            try:
                data = json.load(f)
            except json.JSONDecodeError:
                print(f'⚠️  Invalid JSON: {fn}')
                continue
        if data.get('parent_slug', 'NOT_HUB') == '':
            hub_configs.append((fn, data))

    print(f'Found {len(hub_configs)} hub configs\n')
    patched = 0
    skipped = 0

    for fn, data in hub_configs:
        slug = data.get('slug', '')
        if slug not in HUB_DATA:
            print(f'⚠️  No hub data defined for slug: {slug!r} ({os.path.basename(fn)})')
            skipped += 1
            continue

        hub = HUB_DATA[slug]

        # Only add fields if not already present
        changed = False
        for field in ['intro_answer', 'hub_city_links', 'service_types', 'city_compare_rows']:
            if field not in data:
                data[field] = hub[field]
                changed = True
            else:
                print(f'   ↳ {field} already exists in {os.path.basename(fn)}, skipping')

        if changed:
            with open(fn, 'w', encoding='utf-8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
            print(f'✅ Patched: {os.path.basename(fn)}')
            patched += 1
        else:
            print(f'✓  Already complete: {os.path.basename(fn)}')

    print(f'\nDone. {patched} patched, {skipped} skipped (no data defined).')

if __name__ == '__main__':
    patch_configs()
