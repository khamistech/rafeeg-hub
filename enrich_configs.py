def enrich_config(config_path):
    with open(config_path, 'r', encoding='utf-8') as f:
        c = json.load(f)

    slug = c['slug']
    city = c['city']
    service_slug = c['parent_slug']
    service_name = c['service_name']
    price = c['base_price']
    neighborhoods = CITY_NEIGHBORHOODS.get(city, ["المنطقة المركزية", "الضواحي", "المنطقة الصناعية"])

    # Fix map
    c['map_iframe_src'] = CITY_MAPS.get(city, CITY_MAPS['الشارقة'])
    c['map_coverage']   = CITY_COVERAGE.get(city, f"نغطي جميع أحياء {city}")

    # Enrich body content
    c['body_content'] = get_body(service_slug, service_name, city, neighborhoods, price)

    # Expand personas to 10 (use city-specific if available, else repeat with variation)
    city_personas = PERSONAS.get(service_slug, {}).get(city, None)
    if city_personas and len(city_personas) >= 10:
        c['personas'] = city_personas
    elif city_personas:
        # pad to 10 if needed
        c['personas'] = city_personas
        print(f"  ⚠  only {len(city_personas)} personas for {service_slug}/{city}")

    # Fix links_cards to link to sibling city pages
    other_cities = [ct for ct in CITIES if ct != city]
    c['links_cards'] = [
        {"href": f"/{service_slug}/",                           "title": f"{service_name} الإمارات",   "desc": f"جميع خدمات {service_name}"},
        {"href": f"/{service_slug}-{other_cities[0].replace(' ','-')}/", "title": f"{service_name} {other_cities[0]}", "desc": f"أسعار {other_cities[0]}"},
        {"href": f"/{service_slug}-{other_cities[1].replace(' ','-')}/", "title": f"{service_name} {other_cities[1]}", "desc": f"أسعار {other_cities[1]}"},
        {"href": f"/{service_slug}-{other_cities[2].replace(' ','-')}/", "title": f"{service_name} {other_cities[2]}", "desc": f"أسعار {other_cities[2]}"},
        {"href": "/",                                           "title": "جميع الخدمات",                "desc": "تطبيق رفيق"},
        {"href": "/تركيب-مكيفات-" + city + "/",              "title": f"تركيب مكيفات {city}",          "desc": "خدمات مكيفات"},
    ]

    with open(config_path, 'w', encoding='utf-8') as f:
        json.dump(c, f, ensure_ascii=False, indent=2)


def main():
    count = 0
    for service_slug in NEW_SERVICES:
        for city in CITIES:
            slug = f"{service_slug}-{city.replace(' ', '-')}"
            config_path = f"_configs/{slug}.json"
            if os.path.exists(config_path):
                print(f"  Enriching {slug}...", end=" ", flush=True)
                enrich_config(config_path)
                print("✅")
                count += 1
    print(f"\nEnriched {count} configs")


