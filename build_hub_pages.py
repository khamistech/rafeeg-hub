def main():
    built = 0
    errors = 0
    for service_slug, data in SERVICES.items():
        config = make_hub_config(service_slug, data)
        config_path = f"_configs/{service_slug}.json"

        with open(config_path, "w", encoding="utf-8") as f:
            json.dump(config, f, ensure_ascii=False, indent=2)
        print(f"  📝 Config written: {config_path}")

        result = subprocess.run(
            ["python3", "_build_page.py", config_path],
            capture_output=True, text=True
        )
        if result.returncode == 0:
            print(f"  ✅ Built: {service_slug}/")
            built += 1
        else:
            print(f"  ❌ Error: {service_slug}")
            print(result.stderr[:500])
            errors += 1

    print(f"\n✅ Built {built} hub pages, {errors} errors")


if __name__ == "__main__":
    main()
