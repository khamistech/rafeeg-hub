def generate_city_page(source_file, city, city_data, service_type):
    """Read source HTML and apply city-specific substitutions."""
    with open(source_file, "r", encoding="utf-8") as f:
        html = f.read()

    slug = city_data["slug"]
    source_city = "أبوظبي"
    source_slug_tasleeh = "تصليح-مكيفات-سبلت-أبوظبي"
    source_slug_dukat = "تنظيف-دكتات-مكيفات-أبوظبي"

    if service_type == "tasleeh":
        source_slug = source_slug_tasleeh
    else:
        source_slug = source_slug_dukat

    # Core city name replacements
    html = html.replace(f"/{source_slug}/", f"/{slug}/")
    html = html.replace(source_slug, slug)
    html = html.replace(f"في {source_city}", f"في {city}")
    html = html.replace(f"بـ {source_city}", f"بـ {city}")

    # Hero neighborhoods
    if service_type == "tasleeh":
        html = html.replace(
            "يغطون الريم والخالدية ومصفح",
            f"يغطون {city_data['neighborhoods_hero']}"
        )
        html = html.replace(
            "سكان أبوظبي مقابل",
            f"سكان {city} مقابل"
        )
    else:
        html = html.replace(
            "يغطون الريم آيلاند والخالدية ومصفح",
            f"يغطون {city_data['neighborhoods_hero']}"
        )
        html = html.replace(
            "في أبوظبي المزيد",
            f"في {city} والمزيد"
        )

    # Map src
    html = html.replace(
        'src="https://www.google.com/maps/embed?pb=!1m18!1m12!1m3!1d57588.12640244627!2d55.37137754077148',
        f'src="{city_data["map_src"].split("?")[0]}?'
    )
    # More robust map replacement
    import re
    html = re.sub(
        r'<iframe[^>]*src="https://www\.google\.com/maps/embed[^"]*"',
        f'<iframe loading="lazy" width="100%" height="400" style="border:0;border-radius:var(--radius)" allowfullscreen referrerpolicy="no-referrer-when-downgrade" src="{city_data["map_src"]}"',
        html
    )
    html = html.replace(
        "نغطي جميع مناطق أبوظبي:",
        f"نغطي جميع مناطق {city}:"
    )

    # Map coverage text
    if "الريم، مصفح، الخالدية" in html:
        html = html.replace(
            "الريم، مصفح، الخالدية، بطين، شاطئ الراحة، الوثبة، الشهامة، ومناطق أبوظبي الكبرى والمزيد",
            city_data.get("map_coverage", "").replace("نغطي جميع مناطق " + city + ": ", "")
        )

    # Persona role substitutions
    for old_role, new_role in city_data.get("persona_roles", []):
        html = html.replace(old_role, new_role)

    # Provider addresses
    provider_addrs = city_data.get("provider_addresses", [])
    if len(provider_addrs) >= 3:
        html = html.replace("الريم آيلاند، أبوظبي", provider_addrs[0])
        html = html.replace("الخالدية، أبوظبي", provider_addrs[1])
        html = html.replace("مصفح، أبوظبي", provider_addrs[2])

    # Stats
    if "stats_jobs" in city_data:
        html = html.replace("8,342", city_data["stats_jobs"])
        html = html.replace("9,123", city_data["stats_jobs"])
    if "stats_technicians" in city_data:
        html = html.replace("912 فني", city_data["stats_technicians"] + " فني")

    # Remaining city name instances
    html = html.replace("أبوظبي", city)

    # Fix WhatsApp URL encoding
    if city == "دبي":
        html = html.replace("في+%D8%A3%D8%A8%D9%88%D8%B8%D8%A8%D9%8A", "في+دبي")
        html = html.replace("أبوظبي", "دبي")
    elif city == "الشارقة":
        html = html.replace("في+%D8%A3%D8%A8%D9%88%D8%B8%D8%A8%D9%8A", "في+الشارقة")
    elif city == "عجمان":
        html = html.replace("في+%D8%A3%D8%A8%D9%88%D8%B8%D8%A8%D9%8A", "في+عجمان")

    return html


def make_standalone_city_pages():
    """Generate city-specific variants for تصليح-مكيفات-سبلت and تنظيف-دكتات-مكيفات."""
    tasleeh_source = "تصليح-مكيفات-سبلت-أبوظبي/index.html"
    dukat_source = "تنظيف-دكتات-مكيفات-أبوظبي/index.html"

