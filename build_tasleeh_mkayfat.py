def main():
    print("🔨 Building تصليح-مكيفات pages — 4 cities + hub\n")
    built = []

    for city, data in CITIES.items():
        config = build_city_config(city, data)
        slug = config["slug"]
        config_path = os.path.join(CONFIGS_DIR, f"{slug}.json")

        with open(config_path, "w", encoding="utf-8") as f:
            json.dump(config, f, ensure_ascii=False, indent=2)

        result = subprocess.run(
            [sys.executable, os.path.join(BASE, "_build_page.py"), config_path],
            capture_output=True, text=True, cwd=BASE
        )
        if result.returncode != 0:
            print(f"  ❌ {city}: {result.stderr[:200]}")
            sys.exit(1)

        size = os.path.getsize(os.path.join(BASE, slug, "index.html"))
        print(f"  ✅ {city} — {size:,} bytes → {slug}/index.html")
        built.append(slug)

    # Copy hero images from اصلاح pages (same service, same visuals)
    print("\n  📸 Copying hero images from اصلاح pages...")
    import shutil
    for city, data in CITIES.items():
        slug = f"تصليح-مكيفات-{data['slug_suffix']}"
        src_dir = os.path.join(BASE, f"اصلاح-مكيفات-{data['slug_suffix']}")
        dst_dir = os.path.join(BASE, slug)
        for img in ["hero.jpg", "hero.webp", "ac-before-after.jpg", "ac-before-after.webp"]:
            src = os.path.join(src_dir, img)
            dst = os.path.join(dst_dir, img)
            if os.path.exists(src) and not os.path.exists(dst):
                shutil.copy2(src, dst)
                print(f"    📸 Copied {img} → {slug}/")

    print(f"\n  🎉 Done! Built {len(built)} pages:")
    for s in built:
        print(f"     https://hub.rafeeg.ae/{s}/")
    print(f"\n  ⚠️  Next: run gen_sitemap.py then update homepage internal links")


if __name__ == "__main__":
    main()
